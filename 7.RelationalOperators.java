public class RelationalOperators{
	public static void main(String[] args){
		/*
			关系运算符返回类型为boolean，也就是true或者false
		*/
		int a=30,b=20;
		System.out.println("a>b="+(a>b));
		System.out.println("a<b="+(a<b));
		System.out.println("a=b="+(a==b));
		System.out.println("a!=b="+(a!=b));
		System.out.println("a>=b="+(a>=b));
		System.out.println("a<=b="+(a<=b));
	}
}