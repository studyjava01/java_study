public class DataByte{
	public static void main(String[] args){
		//int
		int i=1000;
		//byte
		byte b=1;
		//short
		short s=2;
		//float
		float f=3.14f;
		//double
		double d=3.1415926;
		//char
		char c='c';
		//boolean
		boolean bool=true;
		
		//print
		System.out.println("int="+i+"\n");
		System.out.println("byte="+b+"\n");
		System.out.println("short="+s+"\n");
		System.out.println("float="+f+"\n");
		System.out.println("double="+d+"\n");
		System.out.println("char="+c+"\n");
		System.out.println("boolean="+bool+"\n");
	}
}
